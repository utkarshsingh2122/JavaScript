// Operators in JS

// Arithmetic operators
let a = 45;
let b = 4;
console.log("a + b = ", a + b)
console.log("a - b = ", a + b)
console.log("a / b = ", a + b)
console.log("a ** b = ", a + b)
console.log("a % b = ", a + b)
console.log("a + b = ", ++b)
console.log("a + b = ", a)
console.log("a-- = ", a--)

