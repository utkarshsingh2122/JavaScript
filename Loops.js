// While Loop
let counter = 0;
while (counter < 10) {
    document.write("Utkarsh" + "\n");
    counter++;
}
// Do While Loop
let counter1 = 0
do {
    document.write("Utkarsh1" + "\n");
    counter1++;
} while (counter1 < 10)

// For Loop
let counter2 = 0;
for (counter2; counter2 <= 10; counter2++) {
    document.write("Utkarsh3" + "\n");
}